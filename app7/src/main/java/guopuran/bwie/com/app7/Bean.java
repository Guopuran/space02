package guopuran.bwie.com.app7;

public class Bean {
    private String name;
    private String pass;

    public Bean(String name, String pass) {
        this.name = name;
        this.pass = pass;
    }

    public Bean() {
    }

    public String getName() {
        return name;
    }

    public String getPass() {
        return pass;
    }
}
